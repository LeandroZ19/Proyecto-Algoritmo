    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int rowDisk = TablaDiscos.getSelectedRow();
        if(rowDisk == -1){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar una fila de la tabla. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        DiscoModel disco = new DiscoModel();
        String diskId = (String) TablaDiscos.getValueAt(rowDisk, 0);
        disco.setDiskId(diskId);
        DiscoDAO dao = new DiscoDAOImpl();
        try {
            dao.delete(disco);
            JOptionPane.showMessageDialog(null, "Eliminado correctamente");
            tablaDiscos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ah ocurrido un error, el no se a eliminado");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

